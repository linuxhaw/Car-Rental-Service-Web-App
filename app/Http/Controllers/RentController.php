     * @return \Illuminate\Http\Response
     */
    public function index()
    {


        if (Auth::check()) {
            
            $rent = DB::select('SELECT r.*,u.name AS user_name,c.name AS car_name
                                FROM 
                                car_rent AS r 
                                JOIN users AS u
                                ON r.user_id = u.id
                                JOIN cars AS c
                                ON r.car_id = c.car_id');
            
            
        return view('rent.index')
        ->with('car_rent', $rent);
    }
    else{
        return redirect()->route('login');
    }
    }

